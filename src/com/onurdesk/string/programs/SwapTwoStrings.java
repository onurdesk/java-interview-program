package com.onurdesk.string.programs;

public class SwapTwoStrings {

	public static void main(String[] args) {
		String x = "om prakash";
		String y = "Suresh kumar";
		String temp = "";
		// using temp variables
		System.out.println("Before Swapping " + x + "," + y);
		temp = x;
		x = y;
		y = temp;
		;
		System.out.println("Before Swapping " + x + "," + y);

	}

}
